package clase_1;

public class EjercicioComplementario {

    public static void main(String[] args) {
        // Declarar una variable para almacenar un mensaje de texto
        String mensajeBienvenida = "Bienvenido a Java";

        // Declarar una variable para representar la temperatura actual como número entero
        int temperaturaActual = 19;

        // Declarar una variable para almacenar un valor lógico
        boolean datoLogico = true;

        // Imprimir las variables con mensajes descriptivos
        System.out.println("Mensaje de bienvenida: " + mensajeBienvenida);
        System.out.println(
            "La temperatura actual es: " + temperaturaActual + "°C"
        );
        System.out.println("El valor lógico es: " + datoLogico);
    }
}
